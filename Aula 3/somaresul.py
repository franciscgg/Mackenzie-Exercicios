num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

soma = num1 + num2
subtracao = num1 - num2
multiplacacao = num1 * num2
divisao = num1 / num2
divisaotruncada = num1 // num2
resto = num1 % num2
exponenciacao = num1 ** num2

print(f'Resultado das operações :'
      f'\nsoma: {soma}\nsubtracao: {subtracao}\nmultiplacacao: {multiplacacao}\ndivisao: {divisao}\ndivisaotruncada: {divisaotruncada}\nresto: {resto}\nexponenciaçao: {exponenciacao}')